    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:

        
        JLabel[] estadoLabels = {lbEstado1, lbEstado2, lbEstado3, lbEstado4, lbEstado5};
        JComboBox[] comboUV = {cbUv1, cbUv2, cbUv3, cbUv4, cbUv5};
        JTextField[] notaFields = {tfNota1, tfNota2, tfNota3, tfNota4, tfNota5};
        double totalNotasUV = 0;
        double totalUV = 0;

        for (int i = 0; i < notaFields.length; i++) {
            double nota = Double.parseDouble(notaFields[i].getText());
            double uv = Double.parseDouble(comboUV[i].getSelectedItem().toString());

            totalNotasUV += nota * uv;
            totalUV += uv;
        }

        if (totalUV == 0) {
            throw new ArithmeticException("No se pueden dividir por cero.");
        }

        double cum = (double) (totalNotasUV / totalUV);

        DecimalFormat formato = new DecimalFormat("#.00");

        String cumFormateado = formato.format(cum);

        tfCum.setText(cumFormateado);
        
        
        for (int i = 0; i < 5; i++) {
            try {
                int nota = Integer.parseInt(notaFields[i].getText());

                if (nota < 0 || nota > 10) {
                    JOptionPane.showMessageDialog(this, "Nota " + (i + 1) + " no vÃ¡lida", "Error", JOptionPane.ERROR_MESSAGE);
                    estadoLabels[i].setText("ESTADO");

                } else {
                    estadoLabels[i].setText(nota > 5 ? "Aprobado" : "Reprobado");

                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Rellene el campo " + (i + 1), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        int materiasAprobadas = 0;
        int materiasReprobadas = 0;

        for (int i = 0; i < 5; i++) {
            int nota = Integer.parseInt(notaFields[i].getText());
            if (nota > 5) {
                materiasAprobadas++;
            } else {
                materiasReprobadas++;
            }
        }

        JComboBox[] Materia = {cbMateria1, cbMateria2, cbMateria3, cbMateria4, cbMateria5};
        String Materias = "";
        String Unidadees = "";
        String Name = "";
        String Apellido = "";

        for (int i = 0; i < Materia.length; i++) {
            if ("Seleccione una opcion".equals(Materia[i].getSelectedItem())) {
                Materias = "-1";
                JOptionPane.showMessageDialog(this, "Seleccione las materias", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        for (int i = 0; i < comboUV.length; i++) {
            if ("0".equals(comboUV[i].getSelectedItem())) {
                Unidadees = "-1";
                JOptionPane.showMessageDialog(this, "Ingrese una Unidad Valorativa Distinta a 0", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

        String nombre = tfNombre.getText();

        if (nombre.isEmpty()) {
            Name = "-1";
            JOptionPane.showMessageDialog(this, "Ingrese un nombre", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        String apellido = tfApellido.getText();

        if (apellido.isEmpty()) {
            Apellido = "-1";
            JOptionPane.showMessageDialog(this, "Ingrese un apellido", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        if (!"-1".equals(Materias) && !"-1".equals(Unidadees) && !"-1".equals(Name)&& !"-1".equals(Apellido)) {
            tfDescripcion.setText(nombre +" "+apellido
                    + " su estado actual es de " + materiasAprobadas
                    + " materias aprobadas y " + materiasReprobadas
                    + " materias reprobadas y su CUM es de " + cumFormateado
            );
        }


    }//GEN-LAST:event_btnCalcularActionPerformed

